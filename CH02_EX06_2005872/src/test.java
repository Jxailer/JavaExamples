
public class test {
	public static void main(String [] ar) {
		//자료형 간 연산
		System.out.println(3+2);
		System.out.println(3-2);
		System.out.println(3*2);
		System.out.println(3/2);
		System.out.println((double)3/(double)2);
		System.out.println((double)(3/2)); // 1.0 (3/2 연산을 한 다음 double형으로 변형했기 때문에.)
		System.out.println((int)3.9+2.2); // 5.2 
		System.out.println((int)(3.9+2.2));
		System.out.println((double)(2.4+(int)5.7));
		
	}
}
